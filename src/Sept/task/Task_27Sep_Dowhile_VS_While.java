package Sept.task;

public class Task_27Sep_Dowhile_VS_While {

    public static void main(String[] args){

        //Give me example of while vs do while loop in Lab ex.

        //while Loop

        int i=0;
        while(i<10)
        {
            System.out.println("Dhivya");
            i++;
        }
        System.out.println("************************************");
    //do while loop

        int j=0;
        do {
            System.out.println("Dhivya");
            j++;
        }while(j<10);
        System.out.println("************************************");
        int a=2;
        while(a<2)
        {
            System.out.println("Dhivya");
            a++;
        }
        System.out.println("************************************");
        //do while loop

        int b=2;
        do {
            System.out.println("Dhivya");
            b++;
        }while(b<2);

    }
}
